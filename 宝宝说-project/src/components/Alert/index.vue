<template>
   <div class="alertbox" v-if="open">
       <div>
           {{tip}}
       </div>

   </div>
</template>
<script>
export default {
   name: 'message',
   props:{
       tip:{
           type:String,
           defalut:"弹框信息"
       },
       time:{
           type:Number,
           defalut:3
       }
   },

   data() {
       return {
           timer:null,
           open:true

       }
   },
  created: {},
  methods: {},
  mounted() {
      this.timer=setTimeout(() => {
          this.open=false
          
      },this.time*1000);
  },
  beforeDestroy() {
      clearTimeout(this.timer)  
  },
}
</script>
